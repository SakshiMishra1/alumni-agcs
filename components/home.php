        <h2 class="ui header">MODx & Semantic Integration</h2>
        <p><em>Themantic is the love child between MODx and Semantic UI.</em></p>
        <p>With Themantic, you'll have access to Semantic's components library in the form of MODx Elements called Snippets and Chunks. These Elements contain Property Values which allow you to specify how to use Semantic's Component's Definitions like Types, Variations, States, etc.</p>
        <p>Snippets will allow for more dynamic output of Semantic's Components whereas Chunks are the static bare bones of Symantic's syntax.</p>
